/** 갤러리 카드에서 쓰는 아이콘 키 — 컴포넌트에서 lucide 아이콘으로 매핑 */
export type ResearchTemplateIcon =
  | "proposal"
  | "survey"
  | "ethics"
  | "presentation";

export interface ResearchTemplate {
  /** 안정적 식별자 */
  id: string;
  /** 카드 제목 */
  title: string;
  /** 카드 한 줄 설명 */
  description: string;
  /** 카드 아이콘 키 */
  icon: ResearchTemplateIcon;
  /** 예상 소요·분량 등 보조 메타(칩) */
  meta: string;
  /** 생성될 노트의 제목 */
  noteTitle: string;
  /** 생성될 노트에 붙는 태그 */
  tags: string[];
  /** 노트 본문에 삽입되는 마크다운 틀 */
  body: string;
}

