export interface ResearchMethodSeedResult {
  created: number;
  updated: number;
  skipped: number;
}

/**
 * 신규 항목은 draft 로 생성하고, 기존 항목 중 절차(procedures)가 없거나
 * 예시(example)가 비어 있는 것은 seed 값으로 동기화한다 (published 상태는 유지).
 * 사이클 119: 양적·질적 11종 절차 신규 + 개발연구 절차 예시 보강을 재시드로 반영하기 위함.
 */
export async function seedResearchMethods(
  userId: string,
  existing: ResearchMethod[],
): Promise<ResearchMethodSeedResult> {
  let created = 0;
  let updated = 0;
  let skipped = 0;
  for (const entry of SEED_RESEARCH_METHODS) {
    const procedures = entry.procedures?.map((p, i) => ({
      id: `p${i + 1}`,
      step: p.step,
      detail: p.detail,
      example: p.example,
    }));
    const found = existing.find((m) => m.name.trim() === entry.name.trim());
    if (found) {
      const needsSync =
        (procedures?.length ?? 0) > 0 &&
        (!found.procedures?.length || found.procedures.some((p) => !p.example));
      if (needsSync) {
        await researchMethodsApi.update(found.id, {
          summary: entry.summary,
          accessibleSummary: entry.accessibleSummary,
          procedures,
        });
        updated += 1;
      } else {
        skipped += 1;
      }
      continue;
    }
    await researchMethodsApi.create({
      name: entry.name,
      kind: entry.kind,
      summary: entry.summary,
      accessibleSummary: entry.accessibleSummary,
      procedures,
      published: false,
      createdBy: userId,
    });
    created += 1;
  }
  return { created, updated, skipped };
}
